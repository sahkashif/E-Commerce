    {
        $cart = Session::get('cart');
        $cart->shippingCharge = $request->input('shippingCharge');
        $cart->grandTotal = $request->input('total');
        $request->session()->put('cart',$cart);

        //dd(Session::get('cart'));
        return view('checkout.index')->with([
            'cart' => $cart
        ]);
    }

